def bucket_depth(v):
    """Bucket turn_index into readable ranges."""
    if v is None: return "Unknown"
    v = int(v)
    if v == 0:   return "0"
    if v <= 2:   return "1-2"
    if v <= 5:   return "3-5"
    if v <= 10:  return "6-10"
    if v <= 20:  return "11-20"
    if v <= 50:  return "21-50"
    return "50+"

def bucket_hist(v):
    """Bucket selected_history length."""
    if v is None: return "Unknown"
    if v == 0:  return "0"
    if v <= 2:  return "1-2"
    if v <= 5:  return "3-5"
    if v <= 10: return "6-10"
    if v <= 15: return "11-15"
    return "16+"

DEPTH_ORDER = ["0","1-2","3-5","6-10","11-20","21-50","50+","Unknown"]
HIST_ORDER  = ["0","1-2","3-5","6-10","11-15","16+","Unknown"]

def compute_stats(recs, mode):
    df = "failure_dimension" if mode == "bad" else "success_dimension"
    dim_cnt  = Counter((r.get(df) or "Unknown") for r in recs)
    diff_cnt = Counter((r.get("difficulty") or "Unknown") for r in recs)
    sc_cnt   = Counter(str(r.get("_llm_score") or "") for r in recs)
    ja_vals  = [float(r["judge_average"]) for r in recs if r.get("judge_average")]

    # Conversation structure
    depth_cnt = Counter()
    hist_cnt  = Counter()
    for r in recs:
        sq = r.get("source_query") or {}
        ti = sq.get("turn_index") if isinstance(sq, dict) else None
        depth_cnt[bucket_depth(ti)] += 1
        hist_cnt[bucket_hist(len(r.get("selected_history") or []))] += 1

    # Scenario & signal
    scenario_cnt   = Counter((r.get("scenario") or "Unknown") for r in recs)
    signal_type_cnt= Counter((r.get("signal_type") or "Unknown") for r in recs)

    # Quality signals
    sig_conf_cnt = Counter((r.get("signal_confidence") or "Unknown") for r in recs)
    repr_cnt     = Counter((r.get("representativeness") or "Unknown") for r in recs)
    oq_cnt       = Counter((r.get("overall_quality") or "Unknown") for r in recs)

    # Mode-specific
    extra = {}
    if mode == "bad":
        sev_cnt = Counter((r.get("severity_tier") or "None") for r in recs)
        extra["severity_cnt"] = dict(sorted(sev_cnt.items(), key=lambda x: -x[1]))

        # judge_scores 5-axis averages
        axes = ["query_completeness","signal_credibility","representativeness","severity","annotation_clarity"]
        sums = {a: 0.0 for a in axes}
        cnts = {a: 0   for a in axes}
        for r in recs:
            js = r.get("judge_scores") or {}
            for a in axes:
                v = js.get(a)
                if v is not None:
                    try:
                        sums[a] += float(v); cnts[a] += 1
                    except: pass
        extra["judge_scores_avg"] = {a: round(sums[a]/cnts[a], 2) if cnts[a] else 0 for a in axes}

        # needs_context / needs_image
        nc = sum(1 for r in recs if r.get("needs_context"))
        ni = sum(1 for r in recs if r.get("needs_image"))
        extra["needs_context_pct"] = round(nc / len(recs) * 100, 1) if recs else 0
        extra["needs_image_pct"]   = round(ni / len(recs) * 100, 1) if recs else 0
        extra["needs_context_n"]   = nc
        extra["needs_image_n"]     = ni
    else:
        sentiment_cnt = Counter((r.get("sentiment") or "Unknown") for r in recs)
        extra["sentiment_cnt"] = dict(sorted(sentiment_cnt.items(), key=lambda x: -x[1]))

    # Sort depth/hist by canonical order
    depth_sorted = {k: depth_cnt[k] for k in DEPTH_ORDER if k in depth_cnt}
    hist_sorted  = {k: hist_cnt[k]  for k in HIST_ORDER  if k in hist_cnt}

    return {
        "total":           len(recs),
        "dim_cnt":         dict(sorted(dim_cnt.items(), key=lambda x: -x[1])),
        "diff_cnt":        dict(diff_cnt),
        "sc_cnt":          dict(sorted(sc_cnt.items(), reverse=True)),
        "ja_avg":          round(sum(ja_vals)/len(ja_vals), 2) if ja_vals else 0,
        "depth_cnt":       depth_sorted,
        "hist_cnt":        hist_sorted,
        "scenario_cnt":    dict(sorted(scenario_cnt.items(), key=lambda x: -x[1])),
        "signal_type_cnt": dict(sorted(signal_type_cnt.items(), key=lambda x: -x[1])),
        "sig_conf_cnt":    dict(sorted(sig_conf_cnt.items(), key=lambda x: -x[1])),
        "repr_cnt":        dict(sorted(repr_cnt.items(), key=lambda x: -x[1])),
        "oq_cnt":          dict(sorted(oq_cnt.items(), key=lambda x: -x[1])),
        **extra,
    }

def filter_recs(recs, mode, dim, diff, score, q):
    df = "failure_dimension" if mode == "bad" else "success_dimension"
    out = []
    for r in recs:
        if dim   and r.get(df)          != dim:   continue
        if diff  and r.get("difficulty") != diff:  continue
        if score and str(r.get("_llm_score") or "") != score: continue
        if q:
            sq  = r.get("source_query") or {}
            txt = (sq.get("message","") if isinstance(sq,dict) else str(sq))
            hay = (txt+" "+(r.get(df) or "")+" "+(r.get("explanation") or "")+" "+(r.get("judge_audit") or "")).lower()
            if q.lower() not in hay: continue
        out.append(r)
    return out

