def build_tag_stats():
    """Scan raw source files once and cache interest_tags / output_category distributions."""
    bad_cids  = {r["cid"] for r in BAD_RECS}
    good_cids = {r["cid"] for r in GOOD_RECS}

    # --- BAD: output_category1/2/3 + interest_tags from dialogues_labeled_0420.jsonl ---
    bad_oc1_ctr = Counter(); bad_oc2_ctr = Counter(); bad_it_ctr = Counter()
    if RAW_BAD_PATH and RAW_BAD_PATH.exists():
        print("Building BAD tag stats from raw data…")
        with open(RAW_BAD_PATH, encoding="utf-8") as f:
            for line in f:
                if not line.strip(): continue
                rec = json.loads(line)
                cid = rec.get("cid")
                if cid not in bad_cids: continue
                meta_str = rec.get("metadata", "{}")
                meta = json.loads(meta_str) if isinstance(meta_str, str) else (meta_str or {})
                turns = meta.get("turns", [])
                # user-profile interest_tags
                up_str = meta.get("user_profile") or "{}"
                up = json.loads(up_str) if isinstance(up_str, str) else (up_str or {})
                for tag in (up.get("interest_tags") or "").split(","):
                    t = tag.strip()
                    if t: bad_it_ctr[t] += 1
                # output_category from last assistant turn
                for t in reversed(turns):
                    if t.get("role") == "assistant":
                        tm = t.get("metadata", {})
                        if isinstance(tm, str): tm = json.loads(tm) if tm else {}
                        oc1 = tm.get("output_category1")
                        oc2 = tm.get("output_category2")
                        if oc1:
                            bad_oc1_ctr[oc1] += 1
                            if oc2: bad_oc2_ctr[oc2] += 1
                        break
        print(f"BAD tag stats done: oc1={sum(bad_oc1_ctr.values())}, it={sum(bad_it_ctr.values())}")

    # --- GOOD: interest_tags from liked-conversations.jsonl user_profile ---
    good_it_ctr = Counter()
    if RAW_GOOD_PATH and RAW_GOOD_PATH.exists():
        print("Building GOOD tag stats from raw data…")
        with open(RAW_GOOD_PATH, encoding="utf-8") as f:
            for line in f:
                if not line.strip(): continue
                rec = json.loads(line)
                cid = rec.get("cid")
                if cid not in good_cids: continue
                up_str = rec.get("user_profile", "{}")
                up = json.loads(up_str) if isinstance(up_str, str) else (up_str or {})
                for tag in (up.get("interest_tags") or "").split(","):
                    t = tag.strip()
                    if t: good_it_ctr[t] += 1
        print(f"GOOD tag stats done: it={sum(good_it_ctr.values())}")

    return {
        "bad": {
            "output_category1": dict(sorted(bad_oc1_ctr.items(), key=lambda x: -x[1])),
            "output_category2": dict(sorted(bad_oc2_ctr.items(), key=lambda x: -x[1])),
            "interest_tags":    dict(sorted(bad_it_ctr.items(),  key=lambda x: -x[1])),
        },
        "good": {
            "output_category1": {},
            "output_category2": {},
            "interest_tags":    dict(sorted(good_it_ctr.items(), key=lambda x: -x[1])),
        },
    }

# Build once at startup (runs in ~3s)
