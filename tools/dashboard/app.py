def _make_blind_case(rec, mode):
    """Extract conversation fields for blind eval, stripping label info."""
    sq  = rec.get("source_query") or {}
    qt  = sq.get("message","") if isinstance(sq,dict) else str(sq) or ""
    cur_idx = sq.get("turn_index") if isinstance(sq,dict) else None
    hist = [{"turn_index": t.get("turn_index"), "role": t.get("role",""),
             "message": extract_response_text(t.get("message","")) if t.get("role","") != "user"
                        else t.get("message","")} for t in (rec.get("selected_history") or [])]
    return {
        "cid":     rec.get("cid",""),
        "mode":    mode,
        "gt":      1 if mode == "good" else -1,   # stored server-side, never sent raw
        "query":   qt,
        "cur_idx": cur_idx,
        "history": hist,
        "response": extract_response_text(rec.get("agent_response_full") or ""),
    }

def _load_history():
    if not BLIND_HISTORY_FILE.exists():
        return []
    try:
        return json.loads(BLIND_HISTORY_FILE.read_text("utf-8"))
    except Exception:
        return []

def _save_history(hist):
    BLIND_HISTORY_FILE.write_text(json.dumps(hist, ensure_ascii=False, indent=2), encoding="utf-8")

def _load_flags() -> set:
    """Return set of flagged CIDs from the JSONL file."""
    if not BLIND_FLAGS_FILE.exists():
        return set()
    cids = set()
    try:
        with open(BLIND_FLAGS_FILE, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line: continue
                try:
                    rec = json.loads(line)
                    cid = rec.get("cid", "")
                    if cid:
                        cids.add(cid)
                except Exception:
                    pass
    except Exception:
        pass
    return cids

def _archive_session(session):
    """Archive a completed blind-eval session into history (skips duplicate CID sets)."""
    result = _compute_blind_result(session)
    if result.get("done", 0) == 0:
        return  # no judgments yet — skip archiving
    cur_cids = frozenset(c["cid"] for c in session["cases"])
    hist = _load_history()
    # If the same CID set already exists in history, skip (prevent duplicate archiving)
    for h in hist:
        if frozenset(h.get("cids", [])) == cur_cids:
            return
    # Build {cid: judgment} map from index-keyed judgments
    judgments_by_cid = {}
    for idx_str, j in session.get("judgments", {}).items():
        try:
            idx = int(idx_str)
            cid = session["cases"][idx]["cid"]
            judgments_by_cid[cid] = int(j)
        except (IndexError, ValueError, KeyError):
            pass
    entry = {
        "created_at": session.get("created_at", ""),
        "archived_at": datetime.now().isoformat(),
        "n": result["total"],
        "done": result["done"],
        "correct": result["correct"],
        "accuracy": result["accuracy"],
        "bad_acc":  result["bad_acc"],
        "good_acc": result["good_acc"],
        "bad_done": result["bad_done"],
        "good_done": result["good_done"],
        "cids": [c["cid"] for c in session["cases"]],
        "judgments_by_cid": judgments_by_cid,
    }
    hist.append(entry)
    _save_history(hist)

def _used_cids():
    """Return the set of CIDs used in all past sessions (to avoid resampling)."""
    hist = _load_history()
    used = set()
    for entry in hist:
        used.update(entry.get("cids", []))
    # Also include current session CIDs (double-safety in case called before archiving)
    if BLIND_SESSION_FILE.exists():
        try:
            cur = json.loads(BLIND_SESSION_FILE.read_text("utf-8"))
            used.update(c["cid"] for c in cur.get("cases", []))
        except Exception:
            pass
    return used

def _create_session():
    used = _used_cids()
    avail_bad  = [r for r in BAD_RECS  if r.get("cid","") not in used]
    avail_good = [r for r in GOOD_RECS if r.get("cid","") not in used]
    # If not enough cases available, relax exclusion to only the last session
    if len(avail_bad) < BLIND_SAMPLE_SIZE or len(avail_good) < BLIND_SAMPLE_SIZE:
        hist = _load_history()
        last_cids = set(hist[-1]["cids"]) if hist else set()
        avail_bad  = [r for r in BAD_RECS  if r.get("cid","") not in last_cids]
        avail_good = [r for r in GOOD_RECS if r.get("cid","") not in last_cids]
    bad_sample  = random.sample(avail_bad,  min(BLIND_SAMPLE_SIZE, len(avail_bad)))
    good_sample = random.sample(avail_good, min(BLIND_SAMPLE_SIZE, len(avail_good)))
    cases = [_make_blind_case(r,"bad") for r in bad_sample] + \
            [_make_blind_case(r,"good") for r in good_sample]
    random.shuffle(cases)
    session = {
        "cases":      cases,
        "judgments":  {},
        "created_at": datetime.now().isoformat(),
    }
    BLIND_SESSION_FILE.write_text(json.dumps(session, ensure_ascii=False, indent=2), encoding="utf-8")
    return session

def _sanitize_session(session):
    """Post-process a loaded session: strip raw JSON from history/response fields."""
    for case in session.get("cases", []):
        # sanitize response
        case["response"] = extract_response_text(case.get("response") or "")
        # sanitize history assistant turns
        for h in case.get("history") or []:
            if h.get("role", "") != "user":
                h["message"] = extract_response_text(h.get("message") or "")
    return session

def _load_session():
    if not BLIND_SESSION_FILE.exists():
        return _create_session()
    try:
        session = json.loads(BLIND_SESSION_FILE.read_text("utf-8"))
        return _sanitize_session(session)
    except Exception:
        return _create_session()

def _save_session(session):
    BLIND_SESSION_FILE.write_text(json.dumps(session, ensure_ascii=False, indent=2), encoding="utf-8")

def _strip_gt(cases):
    """Return cases without the 'gt' field (safe to send to frontend)."""
    return [{"cid": c["cid"], "cur_idx": c["cur_idx"],
             "query": c["query"], "history": c["history"],
             "response": c["response"]} for c in cases]

def _compute_blind_result(session):
    cases     = session["cases"]
    judgments = session["judgments"]
    total  = len(cases)
    done   = len(judgments)
    correct = 0
    bad_ok = bad_tot = good_ok = good_tot = 0
    for i, c in enumerate(cases):
        j = judgments.get(str(i))
        if j is None: continue
        j = int(j)
        gt = c["gt"]
        is_correct = (j == gt)
        if is_correct: correct += 1
        if gt == -1:
            bad_tot += 1
            if is_correct: bad_ok += 1
        else:
            good_tot += 1
            if is_correct: good_ok += 1
    return {
        "total": total, "done": done,
        "correct": correct,
        "accuracy": round(correct/done, 4) if done else None,
        "bad_acc":  round(bad_ok/bad_tot, 4) if bad_tot else None,
        "good_acc": round(good_ok/good_tot, 4) if good_tot else None,
        "bad_done": bad_tot, "good_done": good_tot,
    }

