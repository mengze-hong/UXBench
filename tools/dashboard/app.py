def extract_response_text(raw):
    """Extract clean readable text from an agent_response_full field.

    Supports multiple formats:
    1. Plain string → returned as-is
    2. Hunyuan AgenticSearch JSON (speechType=text/image_with_text):
       {"traceID":..., "content":[{"msg":"...","mark_start":...}, ...], "marks":[...]}
       → Prefers content[].msg/content; falls back to marks[].content if no text found
    3. Legacy format: {"content":[{"msg":"...","type":"text"}], ...}
    """
    import re as _re
    _MARK_RE = _re.compile(r'\[\]\(@[^)]+\)')  # matches [](@mark_underline=N) / [](@replace=N) etc.

    if not raw:
        return ""
    s = raw.strip()
    if not s.startswith("{"):
        return s
    try:
        d = json.loads(s)

        def _clean(text):
            text = _MARK_RE.sub("", text)
            text = _re.sub(r'\n{3,}', '\n\n', text).strip()
            return text

        # ── Extract from content[] ──────────────────────────────────
        content = d.get("content", [])
        if isinstance(content, list):
            parts = []
            image_prompts = []
            for item in content:
                itype = item.get("type", "")
                if itype == "image":
                    p = item.get("prompt") or item.get("promptHint") or ""
                    if p:
                        image_prompts.append(p)
                    continue
                if itype in ("searchGuid", "imageGuid"):
                    p = item.get("prompt") or item.get("botPrompt") or ""
                    if p:
                        image_prompts.append(p)
                    continue
                # deepSearch: nested contents[].msg
                if itype == "deepSearch":
                    for sub in (item.get("contents") or []):
                        sub_msg = sub.get("msg") or ""
                        if sub_msg:
                            parts.append(sub_msg)
                    continue
                text = item.get("msg") or ""
                if not text:
                    c = item.get("content") or ""
                    if isinstance(c, str) and c.strip():
                        text = c
                if text:
                    parts.append(text)
            if parts:
                return _clean("\n".join(parts))
            # Pure image reply: use image description as placeholder
            if image_prompts:
                return f"[AI 回复了图片：{image_prompts[0]}]"

        # ── Fallback: extract from marks[] (marks stores full-text copy) ──────────
        marks = d.get("marks", [])
        if isinstance(marks, list):
            for mk in marks:
                c = mk.get("content") or ""
                if c and not c.strip().startswith("{"):
                    return _clean(c)

        # ── Top-level text field ─────────────────────────────────────────
        top_text = d.get("text") or d.get("msg") or ""
        if top_text:
            return _clean(top_text)

    except Exception:
        pass
    return s

def load_jsonl(p):
    recs = []
    if not Path(p).exists(): return recs
    with open(p, encoding="utf-8") as f:
        for line in f:
            l = line.strip()
            if l:
                try: recs.append(json.loads(l))
                except: pass
    return recs

def save_jsonl(p, recs):
    with open(p, "w", encoding="utf-8") as f:
        for r in recs: f.write(json.dumps(r, ensure_ascii=False) + "\n")

def append_jsonl(p, rec):
    with open(p, "a", encoding="utf-8") as f:
        f.write(json.dumps(rec, ensure_ascii=False) + "\n")

