@app.get("/api/records")
def api_records(mode: str="bad", dim: str="", diff: str="", score: str="",
                q: str="", page: int=0, page_size: int=10):
    recs = BAD_RECS if mode == "bad" else GOOD_RECS
    df   = "failure_dimension" if mode == "bad" else "success_dimension"
    filtered = filter_recs(recs, mode, dim, diff, score, q)
    total = len(filtered)
    start = page * page_size
    page_recs = filtered[start:start+page_size]
    items = []
    for i, r in enumerate(page_recs):
        sq = r.get("source_query") or {}
        qt = (sq.get("message","") if isinstance(sq,dict) else str(sq) or "")[:150]
        rp = extract_response_text(r.get("agent_response_full") or "")[:120]
        items.append({"idx": start+i, "cid": r.get("cid",""),
                      "dim": r.get(df,""), "diff": r.get("difficulty",""),
                      "score": r.get("_llm_score"), "ja": r.get("judge_average"),
                      "query": qt, "preview": rp})
    stats = compute_stats(recs, mode)
    return JSONResponse({**stats, "total": total, "grand_total": len(recs), "items": items})

@app.get("/api/detail")
def api_detail(mode: str="bad", cid: str=""):
    recs = BAD_RECS if mode == "bad" else GOOD_RECS
    df   = "failure_dimension" if mode == "bad" else "success_dimension"
    rec  = next((r for r in recs if r.get("cid")==cid), None)
    if not rec: return JSONResponse({"error": "not found"})
    sq = rec.get("source_query") or {}
    qt = sq.get("message","") if isinstance(sq,dict) else str(sq) or ""
    cur_idx = sq.get("turn_index") if isinstance(sq,dict) else None
    hist = [{"turn_index": t.get("turn_index"), "role": t.get("role",""),
             "message": extract_response_text(t.get("message","")) if t.get("role","") != "user"
                        else t.get("message","")} for t in (rec.get("selected_history") or [])]
    return JSONResponse({
        "cid": rec.get("cid",""), "dim": rec.get(df,""),
        "diff": rec.get("difficulty",""), "score": rec.get("_llm_score"),
        "ja": rec.get("judge_average"), "gpt51": rec.get("gpt51_verdict"),
        "gpt52": rec.get("gpt52_verdict"), "gemini": rec.get("gemini_verdict"),
        "explanation": rec.get("explanation") or rec.get("judge_audit") or "",
        "praise": rec.get("praise_snippet") or "",
        "sig_type": rec.get("signal_type") or "",
        "scenario": rec.get("scenario") or "",
        "query": qt, "current_turn_idx": cur_idx,
        "history": hist, "response": extract_response_text(rec.get("agent_response_full") or ""),
        "reason": rec.get("_llm_reason") or "",
    })

@app.post("/api/exclude")
async def api_exclude(request: Request):
    global BAD_RECS, GOOD_RECS
    body = await request.json()
    mode = body.get("mode","bad")
    cid  = body.get("cid","")
    recs_ref = BAD_RECS if mode=="bad" else GOOD_RECS
    idx  = next((i for i,r in enumerate(recs_ref) if r.get("cid")==cid), None)
    if idx is None: return JSONResponse({"ok": False, "error": "not found"})
    rec = recs_ref.pop(idx)
    rec["_excluded_at"] = datetime.now().isoformat()
    exc_path = EXCL_BAD if mode=="bad" else EXCL_GOOD
    main_path = BAD_PATH if mode=="bad" else GOOD_PATH
    append_jsonl(exc_path, rec)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    shutil.copy(main_path, main_path.parent / f"backup_excl_{ts}.jsonl")
    save_jsonl(main_path, recs_ref)
    return JSONResponse({"ok": True, "remaining": len(recs_ref)})

