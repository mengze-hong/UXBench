@app.get("/api/blind/history/{round_idx}/errors")
def api_blind_history_errors(round_idx: int):
    """Return wrong-judgment cases for a given history round (0-indexed from oldest)."""
    hist = _load_history()
    if round_idx < 0 or round_idx >= len(hist):
        return JSONResponse({"error": "round not found"}, status_code=404)
    entry = hist[round_idx]
    judgments_by_cid = entry.get("judgments_by_cid", {})
    if not judgments_by_cid:
        return JSONResponse({"error": "no judgment data for this round (old entry)", "cases": []})

    # Build CID → (gt, record) lookup from both testsets
    # gt: good=1, bad=-1
    cid_to_rec: dict = {}
    for r in GOOD_RECS:
        cid = r.get("cid","")
        if cid:
            cid_to_rec[cid] = (1, r)
    for r in BAD_RECS:
        cid = r.get("cid","")
        if cid:
            cid_to_rec[cid] = (-1, r)

    # Also look up CIDs stored in this entry's cids list with mode encoded in session
    # (simpler: use GOOD/BAD membership)
    wrong_cases = []
    for cid, judgment in judgments_by_cid.items():
        if cid not in cid_to_rec:
            continue
        gt, rec = cid_to_rec[cid]
        if int(judgment) == gt:
            continue  # correct, skip
        # Wrong — build case data for display
        case = _make_blind_case(rec, "good" if gt == 1 else "bad")
        df = "failure_dimension" if gt == -1 else "success_dimension"
        wrong_cases.append({
            "cid":         cid,
            "gt":          gt,
            "judgment":    int(judgment),
            "query":       case["query"],
            "cur_idx":     case["cur_idx"],
            "history":     case["history"],
            "response":    case["response"],
            "explanation": rec.get("explanation") or rec.get("judge_audit") or "",
            "dim":         rec.get(df, ""),
            "diff":        rec.get("difficulty", ""),
        })

    return JSONResponse({"round": round_idx, "total_wrong": len(wrong_cases), "cases": wrong_cases})

